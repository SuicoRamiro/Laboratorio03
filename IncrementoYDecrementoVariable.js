let a = 3 , b = 2;
let z = a + b;

//INCREMENTO
//Pre-Incremento ( el operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

//Post-Incremento ( el operador ++ despues de la variable)
z = b++;
console.log(b);
console.log(z);

//DECREMENTO
//Pre-Decremento ( el operador -- antes de la variable)
z = --a;
console.log(a);
console.log(z);

//Post-Decremento ( el operador -- despues de la variable)
z = b--;
console.log(b);
console.log(z);